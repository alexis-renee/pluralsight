package com.pluralsight;

public class Person {
    private String FirstName;
    private String LastName;
    private int Age;

    public Person(String firstName, String lastName, int age) {
        FirstName = firstName;
        LastName = lastName;
        Age = age;
    }

    public String getFirstName() {
        return FirstName;
    }

    public String getLastName() {
        return LastName;
    }

    public int getAge() {
        return Age;
    }




    }

